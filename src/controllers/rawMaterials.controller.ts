	async addRawMaterial(req: IGetAuthTypesRequest, res: Response) {
		const user_unique_id: string = req.USER_UNIQUE_ID;

		const errors = validationResult(req);
		const payload = matchedData(req);

		if (!errors.isEmpty()) {
			return ValidationError(res, { unique_id: user_unique_id, text: "Validation Error Occured" }, errors.array());
		}

		const acl_details = await ACL.findOne({
			attributes: { exclude: ['id'] },
			where: {
				user_unique_id: user_unique_id,
				[Op.and]: [
					{ module_unique_id: req.query.module_unique_id },
					{
						...(req.query.sub_module_unique_id ? {
							sub_module_unique_id: req.query.sub_module_unique_id
						} : {})
					}
				],
				acl_expiring: { 
					[Op.or]: [
						{ [Op.gte]: timestamp_str_alt(new Date().setHours(0, 0, 0, 0)) },
						{ [Op.eq]: null }
					]
				},
				status: default_status
			}
		});

		if (!acl_details) {
			return UnauthorizedError(res, { unique_id: user_unique_id, text: "Unauthorized access to record" }, null);
		} 
		
		if (!acl_details.add) {
			return UnauthorizedError(res, { unique_id: user_unique_id, text: "Unauthorized access to add record content" }, null);
		}

		try {
			const raw_material_unique_id = uuidv4();

			await RAW_MATERIAL.sequelize?.transaction(async (transaction) => {
				const rawMaterialResponse = await RAW_MATERIAL.create({
					unique_id: raw_material_unique_id,
					reference: generate_raw_material_reference(), 
					name: payload.name,
					type: payload.type ? payload.type : null,
					description: payload.description ? payload.description : null,
					unit_of_measure: payload.unit_of_measure ? payload.unit_of_measure : null,
					current_quantity: parseInt(payload.current_quantity),
					reorder_level: payload.reorder_level ? parseInt(payload.reorder_level) : zero,
					created_by: user_unique_id,
					status: default_status
				}, { transaction });

				addLog("Raw Materials", user_unique_id, "Added Raw Material", payload);
				if (rawMaterialResponse) {
					return SuccessResponse(res, { unique_id: user_unique_id, text: "Raw Material created successfully!" }, { unique_id: raw_material_unique_id });
				} else {
					throw new Error("Error adding raw material");
				}
			});
		} catch (err: any) {
			return ServerError(res, { unique_id: user_unique_id, text: err.message }, null);
		}
	}

